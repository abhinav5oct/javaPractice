public class sqrPattrn {
    public static void main(String[] args) {
      //print this pattern
        /*
       *******
       *******
       *******
       *******
       */


        int n=5;
        for(int i=1;i<=n;i++){
            for(int j=1;j<=n;j++){
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
